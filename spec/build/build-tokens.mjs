#!/usr/bin/env node
// Liquid Glass token build — reads spec/tokens/*.yaml and emits three
// downstream artifacts under dist/. Style-Dictionary-style pipeline but
// with one tiny custom transformer per target so the kit stays close to
// "no build step" — only one dev dependency (yaml) and one entrypoint.
//
// Inputs:
//   spec/tokens/material.yaml    — glass primitives, tier ladder, roles, budget
//   spec/tokens/shape.yaml       — fixed radii + capsule
//   spec/tokens/spacing.yaml     — gap / margin / section scale
//   spec/tokens/typography.yaml  — fontFamily, weight, scale
//   spec/tokens/motion.yaml      — duration, easing
//
// Outputs (overwrite each run):
//   dist/tokens.css              — CSS custom properties (:root block)
//   dist/Tokens.swift            — Swift enums (Radius, Spacing, Glass, ...)
//   dist/tailwind.tokens.js      — Tailwind theme.extend object
//   dist/tokens.tokensstudio.json — Tokens Studio sync source (for Figma kit, #11)
//
// Flags:
//   --check  exit 1 if regenerating would change any file (CI drift guard)

import { readFileSync, writeFileSync, existsSync, mkdirSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import { parse as parseYaml } from "yaml";

const HERE      = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = resolve(HERE, "..", "..");
const TOKENS    = resolve(REPO_ROOT, "spec", "tokens");
const DIST      = resolve(REPO_ROOT, "dist");

const CHECK_ONLY = process.argv.includes("--check");

const HEADER = `/* Generated by spec/build/build-tokens.mjs. DO NOT EDIT BY HAND.
   Source: spec/tokens/*.yaml. Regenerate with: npm run build:tokens. */
`;
const HEADER_SWIFT = `// Generated by spec/build/build-tokens.mjs. DO NOT EDIT BY HAND.
// Source: spec/tokens/*.yaml. Regenerate with: npm run build:tokens.
`;
const HEADER_JS = `// Generated by spec/build/build-tokens.mjs. DO NOT EDIT BY HAND.
// Source: spec/tokens/*.yaml. Regenerate with: npm run build:tokens.
`;

function loadYaml(relative) {
  return parseYaml(readFileSync(join(TOKENS, relative), "utf8"));
}

const shape      = loadYaml("shape.yaml");
const spacing    = loadYaml("spacing.yaml");
const typography = loadYaml("typography.yaml");
const motion     = loadYaml("motion.yaml");
const material   = loadYaml("material.yaml");

// Map YAML keys to CSS variable suffixes so the generated file matches
// the convention used in examples/macos-web/styles.css (the canonical
// showcase). Without this mapping `controlGap` would land as
// `--lg-gap-controlGap`, which is ugly and forces showcase renames.
const SPACING_VAR = {
  controlGap:          "--lg-gap-control",
  groupGap:            "--lg-gap-group",
  panelGap:            "--lg-gap-panel",
  screenMarginCompact: "--lg-margin-compact",
  screenMarginRegular: "--lg-margin-regular",
  sectionGap:          "--lg-section-gap",
};

function buildCss() {
  const lines = [];
  lines.push(HEADER);
  lines.push(":root {");

  lines.push("  /* Shape — fixed radii (spec/tokens/shape.yaml). */");
  for (const [k, v] of Object.entries(shape.shape.fixed)) lines.push(`  --lg-radius-${k}: ${v}px;`);
  lines.push("  --lg-radius-capsule: 9999px;");

  lines.push("\n  /* Spacing scale (spec/tokens/spacing.yaml). */");
  for (const [k, v] of Object.entries(spacing.spacing)) {
    const varName = SPACING_VAR[k] ?? `--lg-gap-${k}`;
    lines.push(`  ${varName}: ${v}px;`);
  }

  lines.push("\n  /* Motion — durations + easings (spec/tokens/motion.yaml). */");
  for (const [k, v] of Object.entries(motion.motion.duration)) lines.push(`  --lg-duration-${k}: ${v}ms;`);
  for (const [k, v] of Object.entries(motion.motion.easing))   lines.push(`  --lg-ease-${k}: ${v};`);

  lines.push("\n  /* Typography (spec/tokens/typography.yaml). */");
  lines.push(`  --lg-font-sans: ${typography.typography.fontFamily.sans.trim()};`);
  lines.push(`  --lg-font-mono: ${typography.typography.fontFamily.mono.trim()};`);
  for (const [k, v] of Object.entries(typography.typography.weight)) lines.push(`  --lg-weight-${k}: ${v};`);

  lines.push("\n  /* Material — Regular glass / web T1 primitive (spec/tokens/material.yaml). */");
  const reg = material.glass.regular.css;
  lines.push(`  --lg-glass-bg-light: ${reg.background.light};`);
  lines.push(`  --lg-glass-bg-dark: ${reg.background.dark};`);
  lines.push(`  --lg-glass-blur: ${reg.backdropBlur}px;`);
  lines.push(`  --lg-glass-saturation: ${reg.saturation}%;`);
  lines.push(`  --lg-glass-border-light: ${reg.border.light};`);
  lines.push(`  --lg-glass-border-dark: ${reg.border.dark};`);
  lines.push(`  --lg-glass-shadow: ${reg.shadow};`);
  lines.push(`  --lg-glass-highlight: ${reg.highlight};`);

  lines.push("\n  /* Material — Clear glass (requires dim layer behind). */");
  const clr = material.glass.clear.css;
  lines.push(`  --lg-clear-bg-light: ${clr.background.light};`);
  lines.push(`  --lg-clear-bg-dark: ${clr.background.dark};`);
  lines.push(`  --lg-clear-blur: ${clr.backdropBlur}px;`);
  lines.push(`  --lg-clear-saturation: ${clr.saturation}%;`);
  lines.push(`  --lg-clear-border-light: ${clr.border.light};`);
  lines.push(`  --lg-clear-border-dark: ${clr.border.dark};`);
  lines.push(`  --lg-clear-shadow: ${clr.shadow};`);
  lines.push(`  --lg-clear-dim: ${clr.backgroundDim};`);

  lines.push("\n  /* Material — accessibility fallbacks (T0 floor). */");
  const fall = material.fallbacks.reducedTransparency.css;
  lines.push(`  --lg-fallback-bg-light: ${fall.background.light};`);
  lines.push(`  --lg-fallback-bg-dark: ${fall.background.dark};`);
  const cont = material.fallbacks.increasedContrast.css;
  lines.push(`  --lg-fallback-border-light: ${cont.border.light};`);
  lines.push(`  --lg-fallback-border-dark: ${cont.border.dark};`);

  lines.push("}\n");
  return lines.join("\n");
}

function buildSwift({ namespace = "LiquidGlassTokens", asExtension = false } = {}) {
  // Match access level to the enclosing context. The standalone enum form
  // (dist/Tokens.swift) is `public` so external packages can read it.
  // The extension form attaches to the showcase's internal `enum Tokens`,
  // so members must be internal-default to satisfy Swift access rules.
  const acc = asExtension ? "" : "public ";

  const lines = [];
  lines.push(HEADER_SWIFT);
  lines.push("import SwiftUI");
  lines.push("");
  lines.push("/// Liquid Glass design tokens.");
  lines.push("///");
  lines.push("/// Generated from `spec/tokens/*.yaml`. Mirrors the values consumed by");
  lines.push("/// `dist/tokens.css` and `dist/tailwind.tokens.js` so every renderer");
  lines.push("/// pulls from the same source.");
  if (asExtension) {
    lines.push(`extension ${namespace} {`);
  } else {
    lines.push(`public enum ${namespace} {`);
  }

  lines.push(`    ${acc}enum Radius {`);
  for (const [k, v] of Object.entries(shape.shape.fixed)) {
    lines.push(`        ${acc}static let ${k}: CGFloat = ${v}`);
  }
  lines.push(`        ${acc}static let capsule: CGFloat = 9999`);
  lines.push("    }");

  lines.push(`    ${acc}enum Spacing {`);
  for (const [k, v] of Object.entries(spacing.spacing)) {
    lines.push(`        ${acc}static let ${k}: CGFloat = ${v}`);
  }
  lines.push("    }");

  lines.push(`    ${acc}enum Duration {`);
  for (const [k, v] of Object.entries(motion.motion.duration)) {
    lines.push(`        ${acc}static let ${k}: Double = ${v} / 1000.0  // seconds`);
  }
  lines.push("    }");

  lines.push(`    ${acc}enum Weight {`);
  for (const [k, v] of Object.entries(typography.typography.weight)) {
    lines.push(`        ${acc}static let ${k}: Int = ${v}`);
  }
  lines.push("    }");

  lines.push(`    ${acc}enum TypeScale {`);
  for (const [k, v] of Object.entries(typography.typography.scale)) {
    lines.push(`        ${acc}static let ${k}Size: CGFloat = ${v.size}`);
    lines.push(`        ${acc}static let ${k}LineHeight: CGFloat = ${v.lineHeight}`);
    lines.push(`        ${acc}static let ${k}Weight: Int = ${v.weight}`);
  }
  lines.push("    }");

  lines.push("    /// Web-side material approximations. Native code should prefer");
  lines.push("    /// `.glassEffect(.regular)` / `.glassEffect(.clear)` over these numerics;");
  lines.push("    /// they are exposed so multi-target apps can read the same source.");
  lines.push(`    ${acc}enum Glass {`);
  const reg = material.glass.regular.css;
  lines.push(`        ${acc}static let regularBlur: CGFloat = ${reg.backdropBlur}`);
  lines.push(`        ${acc}static let regularSaturation: Int = ${reg.saturation}`);
  const clr = material.glass.clear.css;
  lines.push(`        ${acc}static let clearBlur: CGFloat = ${clr.backdropBlur}`);
  lines.push(`        ${acc}static let clearSaturation: Int = ${clr.saturation}`);
  lines.push("    }");

  lines.push("    /// Performance budget — caps on live-blurred surfaces per pane (B1).");
  lines.push(`    ${acc}enum Budget {`);
  lines.push(`        ${acc}static let recommended: Int = ${material.budget.recommended}`);
  lines.push(`        ${acc}static let busyTransient: Int = ${material.budget.busyTransient}`);
  lines.push(`        ${acc}static let max: Int = ${material.budget.max}`);
  lines.push("    }");

  lines.push("    /// Web renderer tiers (T0–T3). Documented here so cross-platform");
  lines.push("    /// apps can carry the tier label through to telemetry.");
  lines.push(`    ${acc}enum Tier: String, CaseIterable {`);
  lines.push("        case T0, T1, T2, T3");
  lines.push(`        ${acc}static let `+"`default`"+`: Tier = .T1`);
  lines.push("    }");

  lines.push("}\n");
  return lines.join("\n");
}

function buildTailwind() {
  const radius = { ...shape.shape.fixed, capsule: "9999px" };
  const radiusStringified = Object.fromEntries(
    Object.entries(radius).map(([k, v]) => [k, typeof v === "number" ? `${v}px` : v]),
  );
  const spacingPx = Object.fromEntries(
    Object.entries(spacing.spacing).map(([k, v]) => [k, `${v}px`]),
  );
  const duration = Object.fromEntries(
    Object.entries(motion.motion.duration).map(([k, v]) => [k, `${v}ms`]),
  );

  const config = {
    theme: {
      extend: {
        borderRadius: radiusStringified,
        spacing: spacingPx,
        transitionDuration: duration,
        transitionTimingFunction: motion.motion.easing,
        fontFamily: {
          sans: typography.typography.fontFamily.sans.trim().split(/,\s*/),
          mono: typography.typography.fontFamily.mono.trim().split(/,\s*/),
        },
        fontWeight: typography.typography.weight,
        backdropBlur: {
          regular: `${material.glass.regular.css.backdropBlur}px`,
          clear:   `${material.glass.clear.css.backdropBlur}px`,
        },
        backdropSaturate: {
          regular: `${material.glass.regular.css.saturation}%`,
          clear:   `${material.glass.clear.css.saturation}%`,
        },
        colors: {
          "lg-glass-regular-light": material.glass.regular.css.background.light,
          "lg-glass-regular-dark":  material.glass.regular.css.background.dark,
          "lg-glass-clear-light":   material.glass.clear.css.background.light,
          "lg-glass-clear-dark":    material.glass.clear.css.background.dark,
          "lg-fallback-light":      material.fallbacks.reducedTransparency.css.background.light,
          "lg-fallback-dark":       material.fallbacks.reducedTransparency.css.background.dark,
        },
      },
    },
  };

  return `${HEADER_JS}
/** @type {import('tailwindcss').Config['theme']} */
export default ${JSON.stringify(config, null, 2)};
`;
}

function buildTokensStudio() {
  // Tokens Studio (Figma) consumes this shape: nested groups, each leaf
  // is `{ value, type }`. See https://tokens.studio/docs/single-file-json
  // The Figma kit (#11) reads this file via the Tokens Studio plugin.
  const out = {
    radius: { ...mapLeaves(shape.shape.fixed, (v) => ({ value: `${v}px`, type: "borderRadius" })),
              capsule: { value: "9999px", type: "borderRadius" } },
    spacing: mapLeaves(spacing.spacing, (v) => ({ value: `${v}px`, type: "spacing" })),
    duration: mapLeaves(motion.motion.duration, (v) => ({ value: `${v}ms`, type: "duration" })),
    easing: mapLeaves(motion.motion.easing, (v) => ({ value: v, type: "cubicBezier" })),
    fontWeight: mapLeaves(typography.typography.weight, (v) => ({ value: `${v}`, type: "fontWeights" })),
    typeScale: Object.fromEntries(
      Object.entries(typography.typography.scale).map(([k, v]) => [k, {
        fontSize:   { value: `${v.size}px`,       type: "fontSizes" },
        lineHeight: { value: `${v.lineHeight}px`, type: "lineHeights" },
        fontWeight: { value: `${v.weight}`,       type: "fontWeights" },
      }]),
    ),
    glass: {
      regular: {
        backgroundLight: { value: material.glass.regular.css.background.light, type: "color" },
        backgroundDark:  { value: material.glass.regular.css.background.dark,  type: "color" },
        borderLight:     { value: material.glass.regular.css.border.light,     type: "color" },
        borderDark:      { value: material.glass.regular.css.border.dark,      type: "color" },
        blur:            { value: `${material.glass.regular.css.backdropBlur}px`, type: "dimension" },
        saturation:      { value: `${material.glass.regular.css.saturation}%`,    type: "dimension" },
        shadow:          { value: material.glass.regular.css.shadow,           type: "boxShadow" },
      },
      clear: {
        backgroundLight: { value: material.glass.clear.css.background.light, type: "color" },
        backgroundDark:  { value: material.glass.clear.css.background.dark,  type: "color" },
        blur:            { value: `${material.glass.clear.css.backdropBlur}px`, type: "dimension" },
        saturation:      { value: `${material.glass.clear.css.saturation}%`,    type: "dimension" },
        dim:             { value: material.glass.clear.css.backgroundDim,    type: "color" },
      },
      fallback: {
        backgroundLight: { value: material.fallbacks.reducedTransparency.css.background.light, type: "color" },
        backgroundDark:  { value: material.fallbacks.reducedTransparency.css.background.dark,  type: "color" },
        borderLight:     { value: material.fallbacks.increasedContrast.css.border.light,       type: "color" },
        borderDark:      { value: material.fallbacks.increasedContrast.css.border.dark,        type: "color" },
      },
    },
    "$themes": [],
    "$metadata": {
      tokenSetOrder: ["radius", "spacing", "duration", "easing", "fontWeight", "typeScale", "glass"],
    },
  };

  return `${JSON.stringify(out, null, 2)}\n`;
}

function mapLeaves(obj, fn) {
  return Object.fromEntries(Object.entries(obj).map(([k, v]) => [k, fn(v)]));
}

// Showcase mirror: the native macOS showcase keeps its own `enum Tokens`
// API (Tokens.Radius, Tokens.Spacing, ...) for ergonomic call sites. We
// emit a parallel file using that namespace so the showcase compiles
// against the same numeric source as downstream consumers, with no
// manual sync. The hand-written Tokens.swift in the showcase declares
// the empty `enum Tokens { }` shell and the helper types (MaterialRole,
// Easing, concentricRadius).
const SHOWCASE_SWIFT = resolve(
  REPO_ROOT,
  "examples",
  "macos-native-swift",
  "Sources",
  "LiquidGlassShowcase",
  "Tokens.generated.swift",
);

const TARGETS = [
  { absolute: join(DIST, "tokens.css"),               label: "dist/tokens.css",               build: buildCss },
  { absolute: join(DIST, "Tokens.swift"),             label: "dist/Tokens.swift",             build: () => buildSwift({ namespace: "LiquidGlassTokens" }) },
  { absolute: join(DIST, "tailwind.tokens.js"),       label: "dist/tailwind.tokens.js",       build: buildTailwind },
  { absolute: join(DIST, "tokens.tokensstudio.json"), label: "dist/tokens.tokensstudio.json", build: buildTokensStudio },
  { absolute: SHOWCASE_SWIFT,                         label: "examples/macos-native-swift/.../Tokens.generated.swift",
    build: () => buildSwift({ namespace: "Tokens", asExtension: true }) },
];

if (!existsSync(DIST)) mkdirSync(DIST, { recursive: true });

let drift = false;
for (const t of TARGETS) {
  const out = t.build();
  const existing = existsSync(t.absolute) ? readFileSync(t.absolute, "utf8") : null;
  if (CHECK_ONLY) {
    if (existing !== out) {
      console.error(`drift: ${t.label} would change`);
      drift = true;
    }
  } else {
    writeFileSync(t.absolute, out);
    console.log(`wrote ${t.label} (${out.length} bytes)`);
  }
}

if (CHECK_ONLY) {
  if (drift) {
    console.error("\nRun: npm run build:tokens — then commit dist/.");
    process.exit(1);
  }
  console.log("dist/ is up to date.");
}
